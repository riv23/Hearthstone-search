package com.geminicode.hssc.model;

public class Card {
	private String id;
	private String name;
	private String image;
	private String type;
	private String text;
	private String playerClass;
	private String faction;
	private String rarity;
	private String cost;
	private String attack;
	private String health;
	private String flavor;
	private String artist;
	private String collectible;
	private String race;
	private String howToGetGold;
	private String[] mechanics;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getPlayerClass() {
		return playerClass;
	}

	public void setPlayerClass(String playerClass) {
		this.playerClass = playerClass;
	}

	public String getFaction() {
		return faction;
	}

	public void setFaction(String faction) {
		this.faction = faction;
	}

	public String getRarity() {
		return rarity;
	}

	public void setRarity(String rarity) {
		this.rarity = rarity;
	}

	public String getCost() {
		return cost;
	}

	public void setCost(String cost) {
		this.cost = cost;
	}

	public String getAttack() {
		return attack;
	}

	public void setAttack(String attack) {
		this.attack = attack;
	}

	public String getHealth() {
		return health;
	}

	public void setHealth(String health) {
		this.health = health;
	}

	public String getFlavor() {
		return flavor;
	}

	public void setFlavor(String flavor) {
		this.flavor = flavor;
	}

	public String getArtist() {
		return artist;
	}

	public void setArtist(String artist) {
		this.artist = artist;
	}

	public String getCollectible() {
		return collectible;
	}

	public void setCollectible(String collectible) {
		this.collectible = collectible;
	}

	public String getRace() {
		return race;
	}

	public void setRace(String race) {
		this.race = race;
	}

	public String getHowToGetGold() {
		return howToGetGold;
	}

	public void setHowToGetGold(String howToGetGold) {
		this.howToGetGold = howToGetGold;
	}

	public String[] getMechanics() {
		return mechanics;
	}

	public void setMechanics(String[] mechanics) {
		this.mechanics = mechanics;
	}
}
